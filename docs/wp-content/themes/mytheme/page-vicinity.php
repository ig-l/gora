        <span class="title">Псков</span>
        <div class="clear"></div>
        <div class="left">
            <p>Административный центр Псковской области и Псковского райо-
            на. Расположен на реке Великой при слиянии её с рекой Псковой. Первое упоминание в летописях относится к 903 году, когда из села Выбуты близ Пскова в 903 году киевский князь Игорь взял себе в жёны будущую Равноапостольную княгиню Ольгу. По данным археологии, поселение на месте которого возник город у слияния рек Псковы и Великой, было основано не позднее VI века по Р.Х. Имя Пскова, вместе с Новгородом, Киевом и Ладогой стоит у начала истории русской государственности: в одном из городов Псковской земли по утверждению автора «Повести временных лет» княжил Трувор, брат Рюрика и Синеуса, один из трёх братьев, основателей династии Рюриковичей и древнерусского государства. Здесь столетиями проходила западная граница Руси, здесь, на льду Чудского озера Александр Невский остановил яростный «натиск на восток» немецких рыцарей, здесь ганзейские купцы отдавали европейское серебро и сукно за воск и меха из русских лесов, здесь кипела торговля, звенели мечи, спорили богословы, монахи-подвижники основывали обители, а искусные изографы и строители украшали их чудесными храмами, иконами и книгами.</p>

            <p>В 1348—1510 годах — столица независимой Псковской республи-
            ки. В 1510 году присоединён к Великому княжеству Московскому. До начала XVIII века Псков был одним из крупнейших городов России и Европы, важнейшим оборонительным и торговым центром страны. Псковская крепость, состоящая из 5 крепостных колец, (3 из них сохранены по сей день) делала Псков практически неприступным.</p>
        </div>
        <div class="right">
            <p>На протяжении всей своей многовековой истории Псков не раз становился центром ведения крупных боевых действий, но был взят лишь однажды, не считая оккупации во время обеих мировых войн. После основания Санкт-Петербурга Псков утратил своё главенствующее положение на западных рубежах страны, а после того, как по итогам Северной войны границы сместились далеко на запад и России отошли Рига и Ревель (Таллин), значение Пскова как торгового и оборонительного пункта окончательно упало.</p>

            <p>Во время Великой Отечественной войны город три года был оккупирован немецко-фашистскими захватчиками, за это время погибли 3,5 тысячи мирных граждан. В декабре 2009 года Пскову присвоено звание «Город воинской славы».</p>

            <p>Псков — важный туристический центр Псковской области и северо-запада России. Вот далеко не полный список его достопримечательностей:
