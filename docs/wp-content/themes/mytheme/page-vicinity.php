            кой летописи под 1166 годом. До ХIV века Великие Луки - пограничная земля Великого Новгорода и входит в состав Новгородской республики. Город имел собственное управление: на одной из башен крепости (Воскресенской) висел вечевой колокол.</p>

            <p>В 1478 году Василий III присоединил Великие Луки к Московско-
            му княжеству, как крупный оборонительный центр - "предсердие" Москвы. Здесь была ставка Ивана Грозного во время Ливонской войны. В Смутное время (ХVI в.) Великие Луки неоднократно переходили из рук в руки, город был разорен войсками Самозванца и не скоро смог отстроиться после погромов. В период Северной войны стратегическое значение Великих Лук возросло. По указу Петра I великолукская крепость была реконструирована по чертежам известного русского математика Л.Магницкого.</p>

            <p>С 1777 года Великие Луки становятся уездным городом Псковс-
            кого наместничества, с 1812 года - уездным городом Псковской губернии.
