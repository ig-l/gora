        <div class="clear"></div>
        <div class="left">
            <p>Один из древнейших русских городов. Впервые упоминается в «Повести временных лет» в 862 году.  При княгине Ольге (945—960) Изборск стал пригородом Пскова. С X по XIII века о нём нет известий. Известия о городе становятся частыми с 1233 в связи с нападениями немецких рыцарей. Войны с орденом продолжаются около трёх столетий, до начала XVI века. Частые осады и пожары, сопровождавшие нападения рыцарей, заставляли жителей не раз переселяться с одного места на другое. В 1330 году Изборск был построен на новом месте посадником Селогой вместе с псковичами и изборянами на Жеравьей горе и с того времени до XV века новая крепость выдержала восемь осад и ни разу не была взята неприятелем. Борьбой с крестоносцами Изборск заслужил славу оплота Земли Псковской и Новгородской, даже враги называли его «железным городом».</p>

            <p>В 1510 Изборск вместе с Псковом присоединён к Московскому княжеству. Во время осады Пскова (1581) Изборск был занят Речью Посполитой, однако по Ям-Запольскому миру (1582) возвращён Москве.</p>

